def test_authentication():
    """Test if authentication is working."""
    print("ğŸ” Testing Hugging Face Authentication")
    print("-" * 40)
    
    try:
        # Test authentication
        user_info = whoami()
        username = user_info["name"]
        print(f"âœ… Authenticated as: {username}")
        
        # Test API access
        api = HfApi()
        print(f"âœ… API access working")
        
        return username, api
        
    except Exception as e:
        print(f"âŒ Authentication failed: {e}")
        return None, None


def test_repository_creation(username, api):
    """Test repository creation."""
    print(f"\nğŸ§ª Testing Repository Creation")
    print("-" * 40)
    
    try:
        # Create test repository
        repo_name = "test-openllm-auth"
        repo_id = f"{username}/{repo_name}"
        
        print(f"ğŸ”„ Creating test repository: {repo_id}")
        create_repo(
            repo_id=repo_id,
            repo_type="model",
            exist_ok=True,
            private=True
        )
        print(f"âœ… Repository created successfully")
        
        # Clean up
        print(f"ğŸ”„ Cleaning up test repository...")
        delete_repo(repo_id=repo_id, repo_type="model")
        print(f"âœ… Repository deleted")
        
        return True
        
    except Exception as e:
        print(f"âŒ Repository creation failed: {e}")
        return False


def test_file_upload(username, api):
    """Test file upload."""
    print(f"\nğŸ“¤ Testing File Upload")
    print("-" * 30)
    
    try:
        # Create test file
        test_file = "test_upload.txt"
        with open(test_file, "w") as f:
            f.write("This is a test file for OpenLLM authentication verification.\n")
            f.write("Generated by test_hf_auth.py\n")
        
        # Create test repository
        repo_name = "test-upload-openllm"
        repo_id = f"{username}/{repo_name}"
        
        create_repo(
            repo_id=repo_id,
            repo_type="model",
            exist_ok=True,
            private=True
        )
        
        # Upload test file
        print(f"ğŸ”„ Uploading test file to {repo_id}...")
        api.upload_file(
            path_or_fileobj=test_file,
            path_in_repo="test_file.txt",
            repo_id=repo_id,
            repo_type="model",
            commit_message="Test upload from OpenLLM auth test"
        )
        print(f"âœ… File uploaded successfully")
        
        # Clean up
        print(f"ğŸ”„ Cleaning up...")
        delete_repo(repo_id=repo_id, repo_type="model")
        os.remove(test_file)
        print(f"âœ… Cleanup completed")
        
        return True
        
    except Exception as e:
        print(f"âŒ File upload failed: {e}")
        # Clean up test file if it exists
        if os.path.exists("test_upload.txt"):
            os.remove("test_upload.txt")
        return False


